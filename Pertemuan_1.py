# 1. Print String
print("Hello, World!")

# 2. Print Angka Integer
print(100)

# 3. Print Angka Desimal
print(3.14)

# 4. Print Nilai Boolean
print(True)
print(False)

# 5. Print Banyak Data Sekaligus (Multiple Arguments)
print("Nama:", "Taufik", "Umur:", 20)

# 6. Newline Character (\n)
print("Baris Pertama\nBaris Kedua\nBaris Ketiga")

# 7. Positional Arguments
print("Python", "Programming", "Language")

# 8. Keyword Argument (sep)
print("2025", "06", "01", sep="-")

# 9. Keyword Argument (end)
print("Hello", end=" ")
print("World")

# 10. Kombinasi Positional dan Keyword Argument
print("Nama", ":", "Taufik", sep=" ")

# 11. Print Hasil Perhitungan
print("10 + 5 =", 10 + 5)
print("10 - 5 =", 10 - 5)
print("10 * 5 =", 10 * 5)
print("10 / 5 =", 10 / 5)

# 12. Print Variabel
nama = "Taufik"
umur = 20
tinggi = 170.5

print("Nama :", nama)
print("Umur :", umur)
print("Tinggi :", tinggi)

# 13. Format String (f-string)
print(f"Halo, nama saya {nama} dan umur saya {umur} tahun.")

# 14. Print Karakter Khusus
print("Belajar \"Python\"")
print("C:\\Users\\Taufik")

# 15. Print Tabel Sederhana
print("No\tNama\tNilai")
print("1\tAndi\t90")
print("2\tBudi\t85")
print("3\tCitra\t95")

